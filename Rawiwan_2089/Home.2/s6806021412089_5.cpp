#include <iostream>
using namespace std;

int main() {
    int length, width, area;

    // รับค่าความยาว
    cout << "Enter Length : ";
    cin >> length;

    // รับค่าความกว้าง
    cout << "Enter Width : ";
    cin >> width;

    // คำนวณพื้นที่
    area = length * width;

    // แสดงผลลัพธ์
    cout << "Area of Rectagle = " << area << endl;

    return 0;
}
